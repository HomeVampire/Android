# Write a python code to find the area of a right angled triangle where height and
# base are user input.
def main():
    h=int(input("Enter height of the right angled triange: "))
    b=int(input("Enter base of the right angled triange: "))
    area=(h*b)/2
    print("The area of the right angled triange: ",area)

main()
